package DungeonCrawl.Model;

class Room {
    int roomXStartPos;
    int roomYStartPos;
    int roomHeight;
    int roomWidth;

    public int getRoomXStartPos() {
        return roomXStartPos;
    }

    public int getRoomYStartPos() {
        return roomYStartPos;
    }

    public int getRoomHeight() {
        return roomHeight;
    }

    public int getRoomWidth() {
        return roomWidth;
    }
}
